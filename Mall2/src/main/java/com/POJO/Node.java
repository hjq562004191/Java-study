package com.POJO;

public class Node {
    private int id;
    private String name;
    private String touse;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTouse() {
        return touse;
    }

    public void setTouse(String touse) {
        this.touse = touse;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
